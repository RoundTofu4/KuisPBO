    public void actionPerformed(){
        String nama = fnama.getText();
        String jurusan = fjurusan.getText();
        String nim = fnim.getText();
        String skeintb = tftatabhs.getText();
        String seyd = tfeyd.getText();
        String sstr = tfstruktur.getText();
        String skre = tfkreativitas.getText();
        
        double vkeintb = 0, veyd = 0, vstr = 0, vkreativitas = 0;
        try{vkeintb = Double.parseDouble(skeintb);
        veyd = Double.parseDouble(seyd);
        vstr = Double.parseDouble(sstr);
        vkreativitas = Double.parseDouble(skre);
        }
        catch(NumberFormatException error){
        //error handling
        }
        hitungnilai hitung = new hitungnilai(vkeintb, veyd, vstr, vkreativitas);
        hitung.hitungkartul();
        System.out.println(hitung.getnilai());
        new HasilPenilaian(nama, jurusan, nim, hitung.getnilai()).setVisible(true);
        
        }
    
    
